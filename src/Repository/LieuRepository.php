    public function search($keyword)
    {
        return $this->createQueryBuilder('p')
            ->where('p.nom LIKE :keyword')
            ->orWhere('p.category LIKE :keyword')
            ->orWhere('p.region LIKE :keyword')
            ->setParameter('keyword', '%'.$keyword.'%')
            ->getQuery()
            ->getResult();
    }

